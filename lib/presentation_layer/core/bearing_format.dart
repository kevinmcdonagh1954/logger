/// Enum for different bearing/angle format options
enum BearingFormat {
  dms, // D.M.S format (e.g., 123.4567)
  dmsCompact, // D.MS format
  dm, // D.M format
  dmsNoSeparator, // DMS format without separators
  dmsSymbols, // D M S format with symbols
  dmsSymbolsCompact // DMS format with symbols compact
}
